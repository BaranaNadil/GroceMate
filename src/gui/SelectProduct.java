    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        jButton1.setEnabled(true);
        jButton3.setEnabled(false);
        int row = jTable1.getSelectedRow();

        productID = String.valueOf(jTable1.getValueAt(row, 0));
        jTextField1.setText(String.valueOf(jTable1.getValueAt(row, 1)));
        jComboBox1.setSelectedItem(String.valueOf(jTable1.getValueAt(row, 2)));

        if (evt.getClickCount() == 2) {
           grn.getProductIDJLabel().setText(productID);
           grn.getProductNameJLabel().setText(String.valueOf(jTable1.getValueAt(row, 1)));
           grn.getProductBrandLabel().setText(String.valueOf(jTable1.getValueAt(row, 2)));
           this.dispose();
        }


    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        String brand = String.valueOf(jComboBox1.getSelectedItem());

        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter Product name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (brand.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select the brand of the product", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet result = MySQL.execute("SELECT * FROM `prodect` WHERE `name` LIKE '%" + name + "%' AND `brand_id` = '" + brandsMap.get(brand) + "' ");

                if (result.next()) {
                    JOptionPane.showMessageDialog(this, "This Product Alredy In The System", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                   int condition =  JOptionPane.showConfirmDialog(this, "Do You want to Update Product . ProductID : " + productID , "Information", JOptionPane.WARNING_MESSAGE);
                   if(condition == JOptionPane.OK_OPTION){
                       MySQL.execute("UPDATE `prodect` SET `name` = '"+name+"', `brand_id` = '"+brandsMap.get(brand)+"'"
                               + "WHERE `id` = '"+productID+"'");
                   }
                    SignInDialog.logger.info("Product Updated. ProductID : " + productID);
                    reset();
                }

            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

