    private void loadComboBox() {

        try {

            ResultSet result = MySQL.execute("SELECT * FROM `brand`");

            Vector<String> vector = new Vector();
            vector.add("Select");
            while (result.next()) {
                vector.add(result.getString("name"));
                brandsMap.put(result.getString("name"), result.getString("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (SQLException e) {
            e.printStackTrace();
            SignInDialog.logger.severe(e.getMessage());
        }

    }

    private void loadTable() {

        jButton1.setEnabled(false);

        String name = jTextField2.getText();
        String quary = "SELECT * FROM `prodect` INNER JOIN `brand` ON `prodect`.`brand_id` = `brand`.`id`";

        if (!name.isBlank()) {
            quary = quary + " WHERE `prodect`.`name` LIKE '%" + name + "%'  ";
        }

        try {
            ResultSet result = MySQL.execute(quary);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (result.next()) {
                Vector<String> vector = new Vector<>();

                vector.add(result.getString("id"));
                vector.add(result.getString("name"));
                vector.add(result.getString("brand.name"));

                model.addRow(vector);
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }

    }

